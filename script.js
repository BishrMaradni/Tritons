function setLanguage(lang) {
  document.documentElement.lang = lang;
  currentLang = lang;

  document.querySelectorAll("[data-i18n]").forEach(function(el) {
    var key = el.getAttribute("data-i18n");
    var text = translations[lang][key];
    if (text) el.textContent = text;
  });

  document.querySelectorAll("[data-i18n-html]").forEach(function(el) {
    var key = el.getAttribute("data-i18n-html");
    var html = translations[lang][key];
    if (html) el.innerHTML = html;
  });

  var btn = document.querySelector("[data-lang-toggle]");
  if (btn) {
    btn.textContent = lang === "en" ? "EN" : "DE";
    btn.setAttribute("aria-label", lang === "en" ? "Auf Deutsch wechseln" : "Switch to English");
  }

  validationRules.name.required = translations[lang]["validation.name.required"];
  validationRules.name.minLength[1] = translations[lang]["validation.name.min"];
  validationRules.email.required = translations[lang]["validation.email.required"];
  validationRules.email.pattern[1] = translations[lang]["validation.email.invalid"];
  localStorage.setItem("lang", lang);
}

